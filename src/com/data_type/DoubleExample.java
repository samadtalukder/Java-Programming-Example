package com.data_type;

public class DoubleExample {
    private static int defaultValueOfDouble;

    public static void main(String[] args) {
        double dbl = 3.14167;
        System.out.println("Print Double Value: "+dbl);
        System.out.println("Default value of Double: " + defaultValueOfDouble);
        System.out.println("Maximum value of Double: " + Double.MAX_VALUE);
        System.out.println("Minimum value of Double : " + Double.MIN_VALUE);


    }
}
