package com.data_type;

public class LongExample {
    public static long defaultValueOfLong;

    public static void main(String[] args) {
        long longValue = 65;

        System.out.println("Print Long Value: " + (longValue*600+800-2000));
        System.out.println("Default value of Long: " + defaultValueOfLong);
        System.out.println("Maximum value of Long: " + Long.MAX_VALUE);
        System.out.println("Minimum value of Long : " + Long.MIN_VALUE);


    }
}
